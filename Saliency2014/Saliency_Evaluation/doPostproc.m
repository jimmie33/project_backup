data = configData();
model = {...
    struct('name','AMC','printName','AMC','ext','.png','speed',3),...
    struct('name','SO','printName','SO','ext','.png','speed',2),...
    struct('name','SIA','printName','SIA','ext','.png','speed',14),...
    struct('name','RC','printName','RC','ext','.png','speed', 4),...
    struct('name','GS','printName','GS','ext','.png','speed', 4),...
    struct('name','HSal','printName','HS','ext','.png','speed',2),...
    struct('name','DRFI','printName','DR','ext','.png','speed',0.1),...
    struct('name','FT','printName','FT','ext','.png','speed',90),...
    struct('name','HC','printName','HC','ext','.png','speed',88),...
    };
centermap = getCenterMap([300 300]);
for idxModel = 1:numel(model)
    fprintf('%s: ',model{idxModel}.name);
    for idxData = 1:numel(data)
        indir = fullfile('Salmaps/',data{idxData}.name,['output_' model{idxModel}.name]);
        outdir = fullfile('Salmaps/',data{idxData}.name,['output_' model{idxModel}.name '_post_noConstrast']);
        if ~exist(outdir,'dir')
            mkdir(outdir)
        end
        D = dir(fullfile(indir,'*.png'));
        filelist = {D.name};
        parfor i = 1:numel(filelist)
            I = imread(fullfile(indir,filelist{i}));
            I = MBDPostproc(I,centermap);
            imwrite(I,fullfile(outdir,filelist{i}));
        end
    end
    fprintf('\n');
end